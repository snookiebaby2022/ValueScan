const puppeteer = require('puppeteer');

// ── Shared PDF generator ─────────────────────────────────────────
async function generateToolPDF(title, contentHTML, outputPath) {
  let browser;
  try {
    browser = await puppeteer.launch({ headless: 'new', args: ['--no-sandbox'] });
    const page = await browser.newPage();
    const html = `
      <!DOCTYPE html>
      <html>
      <head><meta charset="UTF-8">
        <style>
          body { font-family: Arial, sans-serif; padding: 40px; color: #333; }
          h1 { color: #7c3aed; margin-bottom: 8px; }
          .subtitle { color: #666; margin-bottom: 24px; font-size: 13px; }
          table { width: 100%; border-collapse: collapse; margin: 16px 0; }
          th { background: #f8fafc; padding: 10px; text-align: left; font-size: 12px; text-transform: uppercase; color: #64748b; border-bottom: 2px solid #e2e8f0; }
          td { padding: 10px; border-bottom: 1px solid #e2e8f0; font-size: 13px; }
          .badge { display: inline-block; padding: 2px 8px; border-radius: 12px; font-size: 11px; font-weight: 600; }
          .badge-green { background: #dcfce7; color: #166534; }
          .badge-amber { background: #fef3c7; color: #92400e; }
          .badge-red { background: #fee2e2; color: #991b1b; }
          .score { font-size: 48px; font-weight: 700; color: #7c3aed; }
          .score-label { font-size: 14px; color: #666; }
          .card { background: #f8fafc; border-radius: 8px; padding: 16px; margin: 8px 0; }
          .card-title { font-size: 14px; font-weight: 600; margin-bottom: 4px; }
          .card-value { font-size: 24px; font-weight: 700; color: #0f172a; }
        </style>
      </head>
      <body>
        <h1>${title}</h1>
        <p class="subtitle">Generated by ValueScan on ${new Date().toLocaleString()}</p>
        ${contentHTML}
      </body>
      </html>
    `;
    await page.setContent(html, { waitUntil: 'networkidle0' });
    await page.pdf({ path: outputPath, format: 'A4', printBackground: true, margin: { top: '20px', right: '20px', bottom: '20px', left: '20px' } });
    return { success: true };
  } catch (err) {
    return { success: false, error: err.message };
  } finally {
    if (browser) await browser.close();
  }
}

// ── CSV helper ───────────────────────────────────────────────────
function toCSV(rows, headers) {
  const all = [headers, ...rows];
  return all.map(r => r.map(c => `"${String(c).replace(/"/g, '""')}"`).join(',')).join('\n');
}

// ── Backlinks Export ─────────────────────────────────────────────
async function exportBacklinksPDF(data, outputPath) {
  const rows = (data.backlinks || []).map(b => `
    <tr><td>${b.domain}</td><td>${b.anchor}</td><td>${b.authority}</td><td>${b.dofollow ? 'Yes' : 'No'}</td><td>${b.firstSeen}</td></tr>
  `).join('');
  const html = `
    <div style="display: flex; gap: 16px; margin-bottom: 24px;">
      <div class="card"><div class="card-title">Total</div><div class="card-value">${data.total || 0}</div></div>
      <div class="card"><div class="card-title">Domains</div><div class="card-value">${data.domains || 0}</div></div>
      <div class="card"><div class="card-title">Dofollow</div><div class="card-value">${data.dofollow || 0}</div></div>
      <div class="card"><div class="card-title">Nofollow</div><div class="card-value">${data.nofollow || 0}</div></div>
    </div>
    <table><thead><tr><th>Domain</th><th>Anchor</th><th>Authority</th><th>Dofollow</th><th>First Seen</th></tr></thead><tbody>${rows || '<tr><td colspan="5" style="text-align:center;color:#999">No backlinks found</td></tr>'}</tbody></table>
  `;
  return generateToolPDF('Backlink Analysis Report', html, outputPath);
}

function exportBacklinksCSV(data) {
  const rows = (data.backlinks || []).map(b => [b.domain, b.url, b.anchor, b.authority, b.dofollow ? 'Yes' : 'No', b.firstSeen]);
  return { success: true, csv: toCSV(rows, ['Domain', 'URL', 'Anchor', 'Authority', 'Dofollow', 'First Seen']) };
}

